# El concepto: Ir más allá de la suma y la resta.

# Reto: Crea dos variables, num1 = 15 y num2 = 4.

# Objetivo: Imprime en consola dos resultados:

# El resto (módulo) de dividir 15 entre 4 (usa el operador %).

# El resultado de elevar 15 a la potencia de 4 (usa el operador **).

num1,num2 = 15, 4

modulo = num1 % num2

potencia = num1 ** num2

print(modulo)
print(potencia)